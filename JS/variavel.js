// Uma variavel pode ser declarada com var,const ou let

var clima = "quente";
clima = "frio";

console.log(clima);

let estacao = "outono";
estacao = "verao";
console.log(estacao);

// A const não pode alterar o valor apos sua declaraçao

const dia = 22;

dia = 12; //Isso vai gerar um erro.

console.log(dia);
